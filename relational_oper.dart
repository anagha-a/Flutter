//relational operators
void main(){
  dynamic age=24;

  print('24 > 25    ${age >  25}');
  print("24 < 25    ${age <  25}");
  print("24 ==25   ${age ==  25}");
  print("24 !=25   ${age !=  25}");
  print("24 >=25   ${age >=  25}");
  print("24 <=25   ${age <=  25}");
}

//output
// 24 > 25    false
// 24 < 25    true
// 24 ==25   false
// 24 !=25   true
// 24 >=25   false
// 24 <=25   true