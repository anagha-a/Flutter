void main(){
  var size="XS";

  if(size=="XS"){
    print("Shirt is Extra small");
  }
  else if(size=="S"){
    print("Shirt is Small");
  }
   else if(size=="M"){
    print("Shirt is Medium");
  }
   else if(size=="L"){
    print("Shirt is Large");
  }
   else if(size=="XL"){
    print("Shirt is Extra Large");
  }
  else{
    print("Try another one");
  }
}
// output
// Shirt is Extra small